/*

  Условные конструкции
  ====================
  
    1) для ввода с клавиатуры надо подключить библиотеку dart:io
    2) все, что вводится с клавиатуры = строка
    3) для получения числа с клавиатуры строку надо парсить в число
      а) для контроля num1.runtimeType = string
      б) для контроля num2.runtimeType = int
      
    4) условные операторы
      >    больше
      <    меньше
      >=   больше или равно
      <=   меньше или равно
      ==   равно
      !=   не равно
      &&   логическое и        true, когда оба операнда истина
      ||   логическое или      true, когда хотя бы один операнд истина
      ^    исключающее или     true, когда операнды разные
      !    отрицание           true, когда операнд ложь
      
    5) если внутри блока if одна строка, то можно не писать {}
    6) оператор if позволяет создавать тернарный оператор "условие ? действие1 : действие2"
    7) для сравнения множества вариантов используют switch ... case
    8) после каждого варианта надо писать break (выход из оператора)
    9) если ни один вариант не подойдет, то надо указать default
    10) тип переменной внутри switch == типу вариантов

*/ 

