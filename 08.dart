  // Задача: определить, будет ли введенное число больше или меньше 5
  
  var num1 = 4;
  if (num1 < 5) {
    print("Ваше число $num1 меньше 5");
  } else {
    print("Ваше число $num1 больше или равно 5");
  }
  
  // Задача: проверить, что введенное число четное
  
  var num2 = 4;
  if (num2.isEven) { 
    print("Да, $num2 четное"); 
  } else {
    print("Нет, $num2 не четное");
  }
  
  // Задача: определить сколько корней у уравнения по дискриминанту
  
  var d = 10;
  if (d < 0) {
    print("Нет корня");
  } else if (d == 0) {
    print("Один корень");
  } else {
    print("Два корня");
  }

  print("");
  d > 0 ? print("Есть корни") : print("Нет корней");
  
  d > 0 
    ? print("Есть корни") 
    : print("Нет корней");
  
  var day = 5;
  switch (day) {
      case 1: print("Понедельник"); break;
      case 2: print("Вторник"); break;
      case 3: print("Среда"); break;
      case 4: print("Четверг"); break;
      case 5: print("Пятница"); break;
      case 6: print("Суббота"); break;
      case 7: print("Воскресенье"); break;
      default: print("Нет такого дня");
  }

}